import turtle 


def square1(x, y, size):
    turtle.penup()
    turtle.goto(x,y)
    turtle.pendown()
    turtle.forward(size)
    turtle.right(90)
    turtle.forward(size)
    turtle.left(-90)
    turtle.forward(size)
    turtle.right(90)
    turtle.forward(size)
    turtle.right(90)

